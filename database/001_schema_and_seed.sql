CREATE EXTENSION IF NOT EXISTS pgcrypto;

CREATE TABLE IF NOT EXISTS users (
  id uuid PRIMARY KEY DEFAULT gen_random_uuid(),
  username text NOT NULL,
  email text NOT NULL UNIQUE,
  password_hash text,
  avatar_url text,
  bio text,
  location text,
  member_since date,
  spoken_languages text,
  identity_verification_status text NOT NULL DEFAULT 'not_verified',
  identity_document_type text,
  legal_first_name text,
  legal_last_name text,
  date_of_birth date,
  nz_driver_licence_number text,
  driver_licence_version_number text,
  licence_front_file_url text,
  licence_back_file_url text,
  created_at timestamptz NOT NULL DEFAULT now(),
  updated_at timestamptz NOT NULL DEFAULT now()
);

CREATE TABLE IF NOT EXISTS listings (
  id integer GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  slug text NOT NULL UNIQUE,
  owner_id uuid NOT NULL REFERENCES users(id) ON DELETE RESTRICT,
  title text NOT NULL,
  description text NOT NULL,
  category text NOT NULL,
  listing_type text NOT NULL,
  country text NOT NULL,
  city text NOT NULL,
  current_location text NOT NULL,
  exchange_method text NOT NULL DEFAULT 'Direct Exchange',
  exchange_timings text[] NOT NULL DEFAULT '{}',
  exchange_types text[] NOT NULL DEFAULT '{}',
  wanted_assets text[] NOT NULL DEFAULT '{}',
  wanted_destinations text[] NOT NULL DEFAULT '{}',
  available_from date,
  available_to date,
  image_label text,
  image_asset_key text,
  status text NOT NULL DEFAULT 'active',
  created_at timestamptz NOT NULL DEFAULT now(),
  updated_at timestamptz NOT NULL DEFAULT now()
);

CREATE TABLE IF NOT EXISTS enquiries (
  id integer GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  listing_id integer NOT NULL REFERENCES listings(id) ON DELETE RESTRICT,
  sender_id uuid NOT NULL REFERENCES users(id) ON DELETE RESTRICT,
  receiver_id uuid NOT NULL REFERENCES users(id) ON DELETE RESTRICT,
  offered_listing_id integer REFERENCES listings(id) ON DELETE RESTRICT,
  enquiry_type text NOT NULL DEFAULT 'Direct Exchange',
  status text NOT NULL DEFAULT 'Pending',
  message text NOT NULL,
  owner_response text,
  sender_email_snapshot text,
  disclaimer_accepted boolean NOT NULL DEFAULT false,
  date_sent date,
  date_received date,
  accepted_at timestamptz,
  declined_at timestamptz,
  cancelled_at timestamptz,
  created_at timestamptz NOT NULL DEFAULT now(),
  updated_at timestamptz NOT NULL DEFAULT now()
);

CREATE INDEX IF NOT EXISTS listings_owner_id_idx ON listings(owner_id);
CREATE INDEX IF NOT EXISTS listings_slug_idx ON listings(slug);
CREATE INDEX IF NOT EXISTS listings_country_city_idx ON listings(country, city);
CREATE INDEX IF NOT EXISTS listings_category_type_idx ON listings(category, listing_type);
CREATE INDEX IF NOT EXISTS enquiries_sender_id_idx ON enquiries(sender_id);
CREATE INDEX IF NOT EXISTS enquiries_receiver_id_idx ON enquiries(receiver_id);
CREATE INDEX IF NOT EXISTS enquiries_listing_id_idx ON enquiries(listing_id);
CREATE INDEX IF NOT EXISTS enquiries_status_idx ON enquiries(status);

